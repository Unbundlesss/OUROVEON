#pragma once

#include "pch.h"

#include "math/rng.h"

namespace math {

//----------------------------------------------------------------------------------------------------------------------
RNG32::RNG32( uint32_t seed )
{
    reseed( seed );
}

//----------------------------------------------------------------------------------------------------------------------
RNG32::RNG32()
{
    // by default, seed with the ms-since-system-start
    reseed( (uint32_t)(::GetTickCount64() & UINT32_MAX) );
}

} // namespace math